function interpolateVertex(p1, p2, v1, v2, threshold) {
    if (Math.abs(threshold - v1) < 0.00001) return [...p1];
    if (Math.abs(threshold - v2) < 0.00001) return [...p2];
    if (Math.abs(v1 - v2) < 0.00001) return [...p1];
    const mu = (threshold - v1) / (v2 - v1);
    return [
        p1[0] + mu * (p2[0] - p1[0]),
        p1[1] + mu * (p2[1] - p1[1]),
        p1[2] + mu * (p2[2] - p1[2])
    ];
}

